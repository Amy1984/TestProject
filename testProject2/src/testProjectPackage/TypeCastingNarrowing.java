package testProjectPackage;

public class TypeCastingNarrowing {

	public static void main(String[] args) {
		
		double a = 9.53;
		int b = (int) a; // Manual Casting double to int type
		
		System.out.println("The value of a = " + a); // 9.53
		System.out.println("The value of b = " + b); // 9

	}

}
