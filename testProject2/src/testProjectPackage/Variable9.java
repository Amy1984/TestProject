package testProjectPackage;

public class Variable9 {

	public static void main(String[] args) {
		
		int a; //Declaration of Local Variable
		a = 10; //Assigning the Value to the Variable
		System.out.println(a); //Calling a Variable
		
		char b = 'h'; //Defining a Variable
		System.out.println(b); // h - Calling a Variable
		
		String c = "a";
		System.out.println(c); //a
		
		String str = "Hello Selenium";
		System.out.println(str); //Hello Selenium
	}

}
