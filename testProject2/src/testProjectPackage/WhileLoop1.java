public class WhileLoop1 {

	public static void main(String[] args) {
		
		int i = 10; // Local Variable
		
		while (i < 20 & i < 12) {
			
			System.out.println(i);
			i++; // If we forget to give increment or decrement, Program will execute till infinite
		}

	}

}
